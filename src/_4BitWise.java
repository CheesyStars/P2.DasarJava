public class _4BitWise {
    public static void main(String[] args) {
        //Mendeklarasikan Variabel
        int x = 5, y = 6;
        //Menampilkan Variabel
        System.out.println("X = " +x);
        System.out.println("Y = " +y);
        System.out.println("X & Y = " +(x&y));
        System.out.println("X | Y = " +(x|y));
        System.out.println("X ^ Y + " +(x^y));
    }
}
